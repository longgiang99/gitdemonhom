package demoGitNhom;

public class GitNhom {
    int id;
    String name;

    public GitNhom(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public GitNhom() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

